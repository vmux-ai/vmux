    fn brand_icon(class: &str, d: &str) -> Element {
        rsx! {
            svg {
                class: "{class}",
                view_box: "0 0 24 24",
                fill: "currentColor",
                xmlns: "http://www.w3.org/2000/svg",
                path { d: "{d}" }
            }
        }
    }

    #[component]
    pub fn PageIconView(
        icon: PageIcon,
        url: String,
        img_class: String,
        icon_class: String,
    ) -> Element {
        if url.starts_with("file:") {
            return type_icon(&url, url.ends_with('/'), &icon_class);
        }
        if let PageIcon::Builtin(builtin) = &icon {
            return builtin_icon(*builtin, &icon_class);
        }
        let favicon_url = icon.favicon_url().to_string();
        rsx! {
            Favicon {
                favicon_url,
                url,
                class: img_class,
                globe_class: icon_class,
            }
        }
    }
